    private void actualizarTabla() {
        try {
            JPAController jpa = new JPAController();

            //Obtener la lista de usuarios activos e inactivos
            List<Paciente> pacientes = jpa.getListaPaciente();
            List<Paciente> pacientesActivos = jpa.getPacientesActivos();
            //Contar los usuarios activos e inactivos
            cantPaciente = jpa.pacienteCount();
            cantPacienteActivos = pacientesActivos.size();

            //actualizar las etiquetas
            lblPacientes.setText("Total de usuarios: " + cantPaciente);
            lblActivos.setText("Actualmente activos:" + cantPacienteActivos);

            //Obtener el modelo de la tabla y limpiar las columnas
            DefaultTableModel dtm = (DefaultTableModel)tabla.getModel();
            dtm.setRowCount(0);

            //Validar si el chk esta activo para mostrar todos los usuarios o no
            if(!chkDesactivados.isSelected()){
                //Ocultando inactivos
                for (Paciente paciente : pacientesActivos) {
                    dtm.addRow(paciente.toArray());
                }
            }else{
                //Mostrando todos
                for (Paciente paciente : pacientes) {
                    dtm.addRow(paciente.toArray());
                }
            }

            //Asignar el modelo a la tabla
            tabla.setModel(dtm);
        } catch (Exception e) {
            System.out.println("error: " + e.getMessage());
        }
    }
    
