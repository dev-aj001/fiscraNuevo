    private void btnRechazarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRechazarActionPerformed
        if(opSeleccionada==0){
            int op = JOptionPane.showConfirmDialog(this, "¿Desea rechazar el ingreso del paciente?\nSu expediente sera guardado como rechazado");

            if(op==JOptionPane.OK_OPTION){
                registrar(2);
                mostrarPnlPacientes();
            }
        }else{
            JOptionPane.showMessageDialog(this, "No puede rechazar a un paciente que ya ha sido aceptado en el sistema");
        }
    }//GEN-LAST:event_btnRechazarActionPerformed

